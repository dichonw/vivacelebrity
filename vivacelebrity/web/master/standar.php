            Nilai pH adalah tolak ukur keseimbangan kadar asam dan basa dalam darah.</br>
            pH tubuh ideal ( netral ) berkisar pada rentang 7,35 – 7,45</br>
            </br>
            Kadar pH yang kurang dari 7 dikatakan bersifat ASAM</br>
            Kadar pH lebih dari 7 dikatakan bersifat BASA</br>
            </br>
            Tingkat pH pada tubuh sangat tergantung dari kondisi tubuh dan pola hidup kita sehari2..
            </br>
            Dengan pola hidup kita yang serba instan sekarang seperti makan junk food, tidak olah raga, kurang tidur dll menyebabkan tubuh kita menjadi asam.</br>
            </br>
            Dan disinilah awal permulaan dari para pemasar fanatik alkali mengambil kesempatan untuk mempromosikan manfaat air alkali yang disebut2 sebagai air kesehatan dan keselamatan berperan sebagai antioksidan yang bisa mencegah dan menyembuhkan berbagai penyakit.
            </br>
            Yang perlu anda tahu adalah belum ada satupun bukti ilmiah ataupun bukti kredibel yang cukup untuk membuktikan itu semua.
            </br>
            Mereka memberikan sugesti bahwa minum air murni atau ditilasi berbahaya karena bersifat asam. FAKTANYA sifat air murni benar2 membuat pengukuran pH tidak berarti dalam tubuh.
            </br>
            Air murni bersifat netral pH 6-7
            </br>
            Karena tidak ada padatan terlarut dalam air murni sehingga kita tidak perlu lagi mempengaruhi tingkat pH ke arah alkali atau basa. Ketika kita minum air, berapapun pH nya dalam waktu singkat ph air akan berubah karena bercampur dengan asam dari air liur dan beberapa detik kemudian segera menyatu dengan enzim pencernaan yang sangat asam dan cairan lambung dalam perut tanpa mempengaruhi pH tubuh anda dengan cara apapun. Singkatnya pH air yang akan menyesuaikan dengan tubuh anda bukan tubuh anda yang menyesuaikan dengan pH air.</br>
            </br>
            </br>
            Dr. Bob Arnot MD (penulis terkenal dalam men’s health journal) mengatakan :
            </br>
            “Katakan tidak pada air alkali. Itu adalah penipuan. Tubuh anda dirancang untuk
            menyesuaikan dirinya dengan keseimbangan pH yang optimal. Ketika anda minum air alkali yang Basa masuk kedalam perut anda maka tubuh akan memproduksi asam untuk menetralisir itu semua. Karena perut dirancang asam untuk dapat mencerna makanan dengan baik, sehingga apabila anda mengkonsumsi air alkali maka perut anda akan memproduksi semakin banyak asam untuk menetralisirnya dan mempertahankan keasaman lambung untuk melindungi dari infeksi dan virus.”
            </br>
