            Standard WHO untuk kemurnian air layak minum dibatasi dengan nilai TDS. Nilai TDS maksimal</br> 
            adalah 40 ppm saja. Lebih dari itu sisa proses tidak sanggup diuraikan dari tubuh untuk di</br>
            keluarkan. Resikonya mengendap di organ vital sehingga dapat menimbulkan berbagai macam penyakit.
            </br>
            </br>
            TDS = Total Disolved Solid (Jumlah Padatan (Logam) yang terlarut dalam Air), satuan ukurannya adalah ppm. Semakin tinggi nilai TDS, semakin banyak jumlah kandungan logam yang terlarut di dalam air yang ikut terminum tubuh. Air Minum yang bebas dari Jumlah (Padatan Logam) yang terlarut dalam Air) dengan nilai TDS adalah 0,00 ppm.
            </br>
            </br>
            Pembagian kategori air menurut total zat padat yang terkandung di dalamnya (TDS) adalah:
            </br>
            </br>
            >100 ppm :bukan Air minum</br>
            10 – 100 ppm : Air minum</br>
            1 – 10 ppm : Air murni</br>
            0 ppm : Air organik
  
